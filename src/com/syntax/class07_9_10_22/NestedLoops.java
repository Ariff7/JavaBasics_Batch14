package com.syntax.class07_9_10_22;

public class NestedLoops {

	public static void main(String[] args) {
		
		

		for(int j=0; j<3; j++) {
			
			for(int i=0; i<3; i++) {
				System.out.println(i);
			System.out.println(j);
		}
	}

		
		for (int j=0; j<5; j++) {
			
			int a=0;
			while(a<3) {
				System.out.println(a);
				a++;
			}
		}
		
	}
}
